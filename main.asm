TITLE "MAIN"
LIST P=16F1826
#INCLUDE "P16F1826.INC"

__CONFIG _CONFIG1,(_FOSC_INTOSC & _WDTE_OFF & _PWRTE_OFF &_MCLRE_ON & _CP_OFF & _CPD_OFF & _BOREN_ON & _CLKOUTEN_OFF & _IESO_OFF & _FCMEN_OFF)

CBLOCK	0X20
	d1
	d2
ENDC

ORG 0X0000
 	GOTO	START
	NOP
ORG 0x0010
START	
	CLRF	STATUS
	CLRF	LATA
	CLRF	LATB
	MOVLB	1
	MOVLW	B'11000110'
	MOVWF	TRISB
	MOVLW	0X3F
	MOVWF	TRISA
	MOVLW	0xF0
	MOVWF	OSCCON
	MOVLB	3
	MOVLW	0
	MOVWF	ANSELB
	MOVWF	ANSELA
	MOVLB	0
	MOVLW	0X01
	BANKSEL	ADCON1	
	MOVLW	B'11110000'
	MOVWF	ADCON1
	BANKSEL	TRISA
	BSF		TRISA, 4
	BANKSEL	ANSELA
	BSF		ANSELA, 4


	;;make sure the analog signal is coming through

LOOP
	CALL	SET_ROLL
	CALL	DO_ADC
	CALL	COMPARE_PITCH_TORQUE
	CALL	SET_PITCH_TORQUE
	GOTO	LOOP
	NOP
DO_ADC
	BANKSEL	ADCON0 ;
	MOVLW 	B'00010001' 
	MOVWF 	ADCON0 		
	CALL 	DELAY
	BSF		ADCON0,ADGO
	BTFSC	ADCON0,ADGO
	GOTO	$-1
	RETURN
COMPARE_PITCH_TORQUE
	BTFSC 	ADRESH,1
	RETLW	1
	BTFSC	ADRESH,0
	RETLW	1
	BTFSC	ADRESL,7
	RETLW	1
	RETLW	0
SET_ROLL
	BANKSEL PORTA
	BTFSS	PORTA,3
	BCF		PORTB,5
	NOP
	NOP
	NOP
	BTFSC	PORTA,3
	BSF		PORTB,5	
	NOP
	NOP
	NOP
	RETURN
SET_PITCH_TORQUE
	BANKSEL PORTB
	BTFSC	WREG,0
	BSF		PORTB,3
	NOP
	NOP
	NOP	
	BTFSS	WREG,0
	BCF		PORTB,3
	NOP
	NOP
	NOP
	RETURN
DELAY
	movlw	0x0B
	movwf	d1
	Delay_0
	decfsz	d1, f
	goto	Delay_0
	goto	$+1
	return
END  